package in.jdsoft.studentmanagement.model;

public class TemplateItem {

	public int templateItemId;
	public String templateItemName;
	public Double templateItemPrice;
	
	public void setTemplateItemId(int templateItemId) {
		this.templateItemId = templateItemId;
	}
	
	public int getTemplateItemId() {
		return templateItemId;
	}
	
	public void setTemplateItemName(String templateItemName) {
		this.templateItemName = templateItemName;
	}
	
	public String getTemplateItemName() {
		return templateItemName;
	}
	
	public void setTemplateItemPrice(Double templateItemPrice) {
		this.templateItemPrice = templateItemPrice;
	}
	
	public Double getTemplateItemPrice() {
		return templateItemPrice;
	}
	
	
}
