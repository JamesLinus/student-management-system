package in.jdsoft.studentmanagement.model;

public class GroupMapping {
   
	public int groupMappingId;
	public int courseSemId;
	public int groupId;
	
	public void setGroupMappingId(int groupMappingId) {
		this.groupMappingId = groupMappingId;
	}
	
	public int getGroupMappingId() {
		return groupMappingId;
	}
	
	
	public void setCourseSemId(int courseSemId) {
		this.courseSemId = courseSemId;
	}
	
	public int getCourseSemId() {
		return courseSemId;
	}
	
	
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
	
	public int getGroupId() {
		return groupId;
	}
	
}
